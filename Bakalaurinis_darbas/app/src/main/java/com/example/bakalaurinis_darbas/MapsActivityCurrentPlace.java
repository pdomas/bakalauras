    /**
     * Manipulates the map when it's available.
     * This callback is triggered when the map is ready to be used.
     */
    // [START maps_current_place_on_map_ready]
    @Override
    public void onMapReady(GoogleMap map) {
        this.map = map;

        LocationService locationService = new LocationService(MapsActivityCurrentPlace.this);
        locationService.getJSONArryOfLocations(new LocationService.VolleyResponseListener() {
            @Override
            public void onError(String message) {}
            @Override
            public void onResponse(ArrayList<String> Location_address_array) {}
            @Override
            public void onResponse(JSONArray LocationJSONList) {
                for (int i=0;i<LocationJSONList.length();i++){
                    try {
                        JSONObject marker = LocationJSONList.getJSONObject(i);
                        map.addMarker(new MarkerOptions().position(new LatLng(marker.getDouble("latitude"), marker.getDouble("longitude"))).title(marker.getString("location_address")+"\nValandinis mokestis: "+ marker.getString("price_per_hour")+"â‚¬ \nSavininko numeris: "+ marker.get("contact_info")));
                    } catch (JSONException e) {
                        e.printStackTrace();
                    }
                }
            }
            @Override
            public void onResponse(JSONObject Location_object) {}
        }, "false");
        // [START_EXCLUDE]
        // [START map_current_place_set_info_window_adapter]
        // Use a custom info window adapter to handle multiple lines of text in the
        // info window contents.
        this.map.setInfoWindowAdapter(new GoogleMap.InfoWindowAdapter() {

            @Override
            // Return null here, so that getInfoContents() is called next.
            public View getInfoWindow(Marker arg0) {
                return null;
            }

            @Override
            public View getInfoContents(Marker marker) {
                // Inflate the layouts for the info window, title and snippet.
                View infoWindow = getLayoutInflater().inflate(R.layout.custom_info_contents,
                        (FrameLayout) findViewById(R.id.map), false);

                TextView title = infoWindow.findViewById(R.id.title);
                title.setText(marker.getTitle());

                TextView snippet = infoWindow.findViewById(R.id.snippet);
                snippet.setText(marker.getSnippet());

                return infoWindow;
            }
        });
        // [END map_current_place_set_info_window_adapter]

        // Prompt the user for permission.
        getLocationPermission();
        // [END_EXCLUDE]

        // Turn on the My Location layer and the related control on the map.
        updateLocationUI();

        // Get the current location of the device and set the position of the map.
        getDeviceLocation();
    }
